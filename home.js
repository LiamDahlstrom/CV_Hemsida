var x = 0

function clickCount1() {
    x += 1;
    clicks = "click " + x;

switch(clicks) {
    case "click 1":

        document.getElementById('work1').style.cssText = 'height: 80vh; margin-top: 10vh;'

        document.getElementById('txt_work1').style.cssText = 'opacity: 100%; height: 60vh'
      
break;
    case "click 2":
            document.getElementById('work1').style.cssText = 'height: 10vh; margin-top: 45vh;'

            document.getElementById('txt_work1').style.cssText = 'opacity: 0%; height: 100%'

            x = 0
      break;
}
}
